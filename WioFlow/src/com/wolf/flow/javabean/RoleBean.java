package com.wolf.flow.javabean;

public class RoleBean {

	private String roleid;
	private String flowid;
	private String nodeename;
	public String getRoleid() {
		return roleid;
	}
	public RoleBean setRoleid(String roleid) {
		this.roleid = roleid;return this;
	}
	public String getFlowid() {
		return flowid;
	}
	public RoleBean setFlowid(String flowid) {
		this.flowid = flowid;return this;
	}
	public String getNodeename() {
		return nodeename;
	}
	public RoleBean setNodeename(String nodeename) {
		this.nodeename = nodeename;return this;
	}
	
}
